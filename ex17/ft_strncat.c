char	*ft_strncat(char *dest, char *sec, int nb)
{
	char *head;

	head = dest;
	while (*head)
		head++;
	while (*sec && nb > 0)
	{
		*head++ = *sec++;
		nb--;
	}
	*head = 0;
	return (dest);
}
