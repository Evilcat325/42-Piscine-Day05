char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char *head;

	head = dest;
	while (*src || n > 0)
	{
		*head++ = *src++;
		n--;
	}
	while (n > 0)
	{
		*head++ = '\0';
		n--;
	}
	return (dest);
}
